PI = 3.14159

def area_circle(radius):
    return PI * radius * radius

def perimeter_circle(radius):
    return 2 * PI * radius

def area_square(side):
    return side * side

def perimeter_square(side):
    return 4 * side

def area_triangle(base, height):
    return 0.5 * base * height

def perimeter_triangle(a, b, c):
    return a + b + c
